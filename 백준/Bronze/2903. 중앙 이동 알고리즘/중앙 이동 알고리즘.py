# 입력받기
N = int(input())

# 행렬로 풀지?
# 한 변에 있는 점은 두 점 사이에 생김
# 변의 길이*변의 길이
# 변의 길이 = 2*변의길이-1
# P(n+1) = P(n)*2 - 1
# P(n+2) = P(n+1)*2 - 1
# (P(n+2) - 1)= 2(P(n+1) -1)
# (P(n) - 1 ) = 2^n 형태의 등비수열
# P(n) = 2^n +1 초항이 2이므로
# P(n) = 2^(n-1) + 1
# 변의 길이 식은 위와 같음
# 점의 갯수는 = 변의길이^2

def P(n):
    return 2**(n) + 1

print(P(N)**2)
